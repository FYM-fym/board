package view;


public class Step {

    int initialX;
    int initialY;
    int laterX;
    int laterY;
    int[][] laterChessboard ;


    public Step(int initialX, int initialY, int laterX, int laterY, int[][] laterChessboard) {
        this.initialX = initialX;
        this.initialY = initialY;
        this.laterX = laterX;
        this.laterY = laterY;
        this.laterChessboard = laterChessboard;











    }

































}